package com.javaconcepts;

/**
 * Topic: JVM, JRE, and JDK
 * ------------------------
 * Short Description:
 * - JVM (Java Virtual Machine): Executes Java bytecode (.class files).
 * - JRE (Java Runtime Environment): Provides libraries + JVM to run Java applications.
 * - JDK (Java Development Kit): Full Java suite (JRE + Development tools like javac).
 *
 * Real-Life Example:
 * When you run a Java program, JVM interprets the bytecode.
 * When you write and compile a Java program, you use JDK.
 */
public class JVM_JRE_JDK {
    public static void run() {
        System.out.println("JVM: Executes bytecode and provides runtime environment.");
        System.out.println("JRE: Contains JVM and standard libraries to run Java apps.");
        System.out.println("JDK: Contains JRE + development tools like compiler, debugger, etc.");
    }
}
